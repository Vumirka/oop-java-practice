package src.domain;

import java.io.IOException;

// ============================================================
// ЗАВДАННЯ 3 - інтерфейс "фабрикованих" об'єктів
// Оголошує методи для відображення результатів обчислень
// ============================================================

/**
 * <h1>View</h1>
 *
 * Інтерфейс Product у шаблоні Factory Method.
 * Оголошує методи відображення результатів обчислень
 * та роботи з колекцією.
 */
public interface View {

    /** Відображає заголовок */
    void viewHeader();

    /** Відображає основну частину - список результатів */
    void viewBody();

    /** Відображає завершення */
    void viewFooter();

    /** Відображає об'єкт повністю */
    void viewShow();

    /** Ініціалізує колекцію результатів */
    void viewInit();

    /**
     * Зберігає колекцію у файл.
     * @throws IOException помилка запису
     */
    void viewSave() throws IOException;

    /**
     * Відновлює колекцію з файлу.
     * @throws Exception помилка читання або клас не знайдено
     */
    void viewRestore() throws Exception;
}