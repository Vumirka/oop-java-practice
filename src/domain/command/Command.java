/**
 * <h1>Command</h1>
 *
 * Інтерфейс команди у шаблоні проектування Command.
 * Кожна команда реалізує метод {@link #execute()} та {@link #undo()}.
 */
public interface Command {

    /**
     * Виконує команду.
     */
    void execute();

    /**
     * Скасовує виконану команду (undo).
     */
    void undo();
}